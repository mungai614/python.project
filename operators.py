#arithmetic operators{add,sub, modulas%
x = 40
y = 32
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #modulas- returns remainder after division

#comparison operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) #equal to
print(x != y)#not equal to

#assignment operators
a = 10
print(a)
a += 2
print(a)
a *= 2
print(a)

#logical operators; and ,or,not (more than 0ne condition) and only returns a true if all the conditions are true
print(34 < 50 and 56 > 200)
print(34 < 50 or 56 > 200)
print(not(34 < 50 or 56 > 200))
